package lessons.conditional_operators;

public class HardConditional {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a == 10 && b > 15) {//true && false -> false, logical AND

        }

        if (a < 10 || b > 15) {//true || false -> true, logical OR

        }
    }
}

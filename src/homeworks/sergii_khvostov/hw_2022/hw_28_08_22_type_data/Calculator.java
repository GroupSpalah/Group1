public class Calculator {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a + b;
        System.out.println("a + b = " + sum);

        int sub = a - b;
        System.out.println("a - b = " + sub);

        int mul = a * b;
        System.out.println("a * b = " + mul);

        int div = a / b;
        System.out.println("a / b = " + div);

        int dwr = a % b;
        System.out.println("a % b = " + dwr);
    }
}
